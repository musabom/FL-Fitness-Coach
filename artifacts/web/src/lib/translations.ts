export const translations: Record<Language, typeof en> = { en, ar };

type DeepValue<T, K extends string> = K extends `${infer Head}.${infer Tail}`
  ? Head extends keyof T
    ? DeepValue<T[Head], Tail>
    : never
  : K extends keyof T
  ? T[K]
  : never;

export type TranslationKey = string;

export function getTranslation(lang: Language, key: string): string {
  const parts = key.split(".");
  let current: Record<string, unknown> = translations[lang] as unknown as Record<string, unknown>;
  for (const part of parts) {
    if (typeof current !== "object" || current === null || !(part in current)) {
      const fallback = translations.en as unknown as Record<string, unknown>;
      let fb = fallback;
      for (const p of parts) {
        if (typeof fb !== "object" || !(p in fb)) return key;
        fb = fb[p] as Record<string, unknown>;
      }
      return typeof fb === "string" ? fb : key;
    }
    current = current[part] as Record<string, unknown>;
  }
  return typeof current === "string" ? current : key;
}
